const radius = 21;
const circleArea = Math.PI * Math.pow(radius, 2);
console.log("Circle area:", circleArea.toFixed(2));


const length = 21;
const width = 7;
const rectangleArea = length * width;
console.log("Area of the rectangle:", rectangleArea.toFixed(2));


const cylinderRadius = 7;
const cylinderHeight = 21;
const cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * cylinderHeight;
console.log("The volume of the cylinder:", cylinderVolume.toFixed(2));