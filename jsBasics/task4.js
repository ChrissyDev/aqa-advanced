let radius = 21;
let circleArea = Math.PI * Math.pow(radius, 2);
console.log("Circle area:", circleArea.toFixed(2));


let length = 21;
let width = 7;
let rectangleArea = length * width;
console.log("Area of the rectangle:", rectangleArea.toFixed(2));


let cylinderRadius = 7;
let cylinderHeight = 21;
let cylinderVolume = Math.PI * Math.pow(cylinderRadius, 2) * cylinderHeight;
console.log("The volume of the cylinder:", cylinderVolume.toFixed(2));